package com.shop.entity;

public class Title_Info {
    private int title_id;
    private String title_name;
    private String title_head;
    private String title_message;
    private String title_date;

    public int getTitle_id() {
        return title_id;
    }

    public void setTitle_id(int title_id) {
        this.title_id = title_id;
    }


    public String getTitle_name() {
        return title_name;
    }

    public void setTitle_name(String title_name) {
        this.title_name = title_name;
    }

    public String getTitle_head() {
        return title_head;
    }

    public void setTitle_head(String title_head) {
        this.title_head = title_head;
    }

    public String getTitle_message() {
        return title_message;
    }

    public void setTitle_message(String title_message) {
        this.title_message = title_message;
    }

    public String getTitle_date() {
        return title_date;
    }

    public void setTitle_date(String title_date) {
        this.title_date = title_date;
    }
}
